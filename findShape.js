function findClosestPixelDistance(X, Y, Z, RT, blockPixelDiscovered){
      let i;
      let iL;
      let distanceTopLeft = 999;
      let distanceTopRight = 999;
      let distanceBotLeft = 999;
      let distanceBotRight = 999;
      
      
      
      for(iL = 0; iL < blockPixelCount; iL++){
            let TL = 999;
            let TR = 999;
            let BL = 999;
            let BR = 999;
            if(!blockPixelDiscovered[iL] && blockPixelColor[iL] == 0){

               //   console.log("blockPixelDiscovered C: " + blockPixelDiscovered[iL]);
                //  console.log(" C: " );

            if((RT & 0x03) == 0 ){ 
                  if(blockPixelX[iL] > X){
                        TL = blockPixelX[iL] - (X + 1) + Math.abs( blockPixelZ[iL] - (Z + 1) ) + Math.abs( blockPixelY[iL] - Y);
                        TR = blockPixelX[iL] - (X + 1) + Math.abs( blockPixelZ[iL] - Z ) + Math.abs( blockPixelY[iL] - Y);
                  }else{
                        BL = X -blockPixelX[iL]  + Math.abs( blockPixelZ[iL] - (Z + 1) ) + Math.abs( blockPixelY[iL] - Y);
                        BR = X -blockPixelX[iL]  + Math.abs( blockPixelZ[iL] - Z ) + Math.abs( blockPixelY[iL] - Y);
                  }
            }else if((RT & 0x03) == 2 ){ 
                  if(blockPixelX[iL] < X){
                        TL = (X - 1) - blockPixelX[iL]  + Math.abs( blockPixelZ[iL] - (Z - 1) ) + Math.abs( blockPixelY[iL] - Y);
                        TR = (X - 1) - blockPixelX[iL]  + Math.abs( blockPixelZ[iL] - Z ) + Math.abs( blockPixelY[iL] - Y);
                  }else{
                        BL = blockPixelX[iL] - X  + Math.abs( blockPixelZ[iL] - (Z - 1) ) + Math.abs( blockPixelY[iL] - Y);
                        BR = blockPixelX[iL] - X  + Math.abs( blockPixelZ[iL] - Z ) + Math.abs( blockPixelY[iL] - Y);
                  }
            }else if((RT & 0x03) == 1 ){ 
                  if(blockPixelZ[iL] < Z){
                        TL = (Z - 1) - blockPixelZ[iL]  + Math.abs( blockPixelX[iL] - (X + 1) ) + Math.abs( blockPixelY[iL] - Y);
                        TR = (Z - 1) - blockPixelZ[iL]  + Math.abs( blockPixelX[iL] - X ) + Math.abs( blockPixelY[iL] - Y);
                  }else{
                        BL = blockPixelZ[iL] - Z  + Math.abs( blockPixelX[iL] - (X + 1) ) + Math.abs( blockPixelY[iL] - Y);
                        BR = blockPixelZ[iL] - Z  + Math.abs( blockPixelX[iL] - X ) + Math.abs( blockPixelY[iL] - Y);
                  }
            }else if((RT & 0x03) == 3 ){ 
                  if(blockPixelZ[iL] > Z){
                        TL =  blockPixelZ[iL] - (Z + 1) + Math.abs( blockPixelX[iL] - (X - 1) ) + Math.abs( blockPixelY[iL] - Y);
                        TR =  blockPixelZ[iL] - (Z + 1)  + Math.abs( blockPixelX[iL] - X ) + Math.abs( blockPixelY[iL] - Y);
                  }else{
                        BL = Z - blockPixelZ[iL] + Math.abs( blockPixelX[iL] - (X - 1) ) + Math.abs( blockPixelY[iL] - Y);
                        BR = Z - blockPixelZ[iL] + Math.abs( blockPixelX[iL] - X ) + Math.abs( blockPixelY[iL] - Y);
                  }
            }

            if(TL < distanceTopLeft){
                  distanceTopLeft = TL;
            }

            if(TR < distanceTopRight){
                  distanceTopRight = TR;
            }

            if(BL < distanceBotLeft){
                  distanceBotLeft = BL;
            }
            if(BR < distanceBotRight){
                  distanceBotRight = BR;
            }
      }




      }

      let distance = 0;
      if(distanceTopLeft < 5 || distanceTopRight < 5 ){
            distance |= 0x04;
      }

      if(distanceBotLeft < 5 || distanceBotRight < 5 ){
            distance |= 0x20;
      }

      return distance;

}